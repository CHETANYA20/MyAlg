	public int getmax(int[] length)
	{
		Arrays.sort(length);
		int total = 0;
		int pre = -1;
		for(int i: length) {
			if(i == pre) {
				total++;
				pre = -1;
			} else
				pre = i;
		}
		return total;
	}
	
