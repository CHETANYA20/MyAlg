	public String identify(String[] plates)
	{
		for(int i = 0; i < plates[0].length(); i++) {
			int ones = 0;
			int zeros = 0;
			for(String s : plates) {
				if(s.charAt(i) == '1')
					ones++;
				else
					zeros++;
			}
			if(ones < 2 || zeros == 0)
				return "NO";
		}
		return "YES";
	}
	
