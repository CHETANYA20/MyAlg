	public String[] playList(int n)
	{
		int k = Math.min(n, 50);
        String[] ret = new String[k];
        int cur = 1;
        for(int i = 0; i < k; i++) {
            ret[i] = cur + ".mp3";
            cur = getNext(cur, n);
        }
        return ret;
	}

    int getNext(int cur, int n) {
        if(cur * 10 <= n)
            return cur*10;
        if((cur % 10) == 9 || (cur+1) > n)
            return cur / 10 + 1;

        return cur+1;
    }
	
