	public int minimumAdditional(String[] plates)
	{
		int n = plates.length;
		int m = plates[0].length();
		int max = 0;
		for(int i = 0; i < m; i++) {
			int ones = 0;
			int zeros = 0;
			for(String s : plates) {
				if(s.charAt(i) == '1')
					ones++;
				else
					zeros++;
			}
			int need = 0;
			if(ones < 2)
				need = 2- ones;
			if(zeros < 1)
				need++;
			max = Math.max(max, need);
			if(max == 3)
				return 3;
		}
		return max;
	}
	
